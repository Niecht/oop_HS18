package oop06;

/**
 * The Operations Interface describes the function of a simple two value calculator.
 * Author: Sven
 * Version: 10.29.2018
 */
public interface Operations {

    /**
     * Allows addition of two values.
     * @param x number in int format
     * @param y number in int format
     * @return the sum of the two parameters
     */
    int addition(int x, int y);
}
